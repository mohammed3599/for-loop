namespace For_Loop
{
    internal class Program
    {
        static void Main(string[] args)
        {
            for(int i = 0; i <= 4; i++)
            {
                    Console.Write("*");
            }
           
            Console.WriteLine();
            
            for(int i = 0; i <= 2; i++)
            {
                Console.WriteLine("*   *");
            }
           
            for(int i = 0; i <= 4; i++)
            {
                Console.Write("*");
            }
        }
    }
}